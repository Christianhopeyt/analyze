const NicheInsights = {
  lang: 'EN',
  data: null,
  init() {
    this.form = document.getElementById('niche-form');
    if (!this.form) return;
    this.lang = localStorage.getItem('yta-lang') === 'FR' ? 'FR' : 'EN';
    this.applyLanguage();
    this.form.addEventListener('submit', event => { event.preventDefault(); this.run(); });
    document.querySelectorAll('.lang-btn').forEach(button => button.addEventListener('click', () => {
      setTimeout(() => { this.lang = button.dataset.lang; this.applyLanguage(); if (this.data) this.render(this.data); }, 0);
    }));
  },
  text(key) { return NI_STRINGS[this.lang]?.[key] || NI_STRINGS.EN[key] || ''; },
  applyLanguage() {
    document.querySelectorAll('[data-ni]').forEach(element => {
      const value = this.text(element.dataset.ni);
      if (value) element.textContent = value;
    });
    document.querySelectorAll('[data-ni-placeholder]').forEach(element => {
      const value = this.text(element.dataset.niPlaceholder);
      if (value) element.placeholder = value;
    });
    document.getElementById('niche-language').value = this.lang === 'FR' ? 'fr' : 'en';
  },
  setStatus(message, error = false, loading = false) {
    const status = document.getElementById('niche-status');
    status.textContent = message;
    status.classList.toggle('error', error);
    status.classList.toggle('loading', loading);
  },
  async run() {
    const query = document.getElementById('niche-query').value.trim();
    if (query.length < 2) { this.setStatus(this.text('invalid'), true); document.getElementById('niche-query').focus(); return; }
    const button = document.getElementById('niche-submit');
    button.disabled = true;
    this.setStatus(this.text('loading'), false, true);
    try {
      const response = await fetch('/api/niche-insights', {
        method: 'POST',
        headers: { 'content-type': 'application/json' },
        body: JSON.stringify({
          query,
          region: document.getElementById('niche-region').value,
          language: document.getElementById('niche-language').value,
          includeAiSuggestions: true
        })
      });
      const data = await response.json();
      if (!response.ok) {
        const errorKey = data.code === 'API_NOT_CONFIGURED' ? 'api_not_configured'
          : data.code === 'RATE_LIMIT' ? 'rate_limited'
          : 'error';
        throw new Error(this.text(errorKey));
      }
      this.data = data;
      this.render(data);
      this.setStatus('');
    } catch (error) {
      this.setStatus(error.message || this.text('error'), true);
    } finally {
      button.disabled = false;
    }
  },
  number(value, digits = 0) {
    if (value === null || value === undefined || !Number.isFinite(Number(value))) return '—';
    return new Intl.NumberFormat(this.lang === 'FR' ? 'fr-FR' : 'en-US', { maximumFractionDigits: digits }).format(value);
  },
  percent(value) { return value === null || value === undefined ? '—' : `${this.number(value, 2)}%`; },
  escape(value) { const div = document.createElement('div'); div.textContent = String(value ?? ''); return div.innerHTML; },
  scoreCard(kind, label, value) {
    return `<article class="niche-score-card ${kind}"><div class="niche-score-label">${this.escape(label)}</div><div class="niche-score-value">${value}</div><div class="niche-score-bar"><div class="niche-score-fill" style="width:${value}%"></div></div></article>`;
  },
  render(data) {
    const analytics = data.analytics;
    const results = document.getElementById('niche-results');
    results.classList.add('visible');
    document.getElementById('report-title').textContent = data.query;
    document.getElementById('report-meta').textContent = `${analytics.sampleSize} ${this.text('sampled')} · ${this.text('updated')} ${new Date(data.sampledAt).toLocaleString(this.lang === 'FR' ? 'fr-FR' : 'en-US')}`;
    document.getElementById('confidence-badge').textContent = `${this.text(analytics.confidence)} ${this.text('confidence')}`;
    this.renderNotices(data);
    if (!analytics.sampleSize) {
      document.getElementById('score-grid').innerHTML = '';
      document.getElementById('metrics-grid').innerHTML = `<div class="niche-empty niche-empty-wide">${this.escape(this.text('empty'))}</div>`;
      document.querySelector('.niche-layout').hidden = true;
      this.setStatus(this.text('empty'), true);
      return;
    }
    document.querySelector('.niche-layout').hidden = false;
    document.getElementById('score-grid').innerHTML = [
      this.scoreCard('trend', this.text('trend'), analytics.scores.trend),
      this.scoreCard('competition', this.text('competition'), analytics.scores.competition),
      this.scoreCard('opportunity', this.text('opportunity'), analytics.scores.opportunity)
    ].join('');
    this.renderMetrics(analytics);
    this.renderWindows(analytics.windows);
    this.renderKeywords(analytics.commonKeywords);
    this.renderPatterns(analytics.titlePatterns);
    this.renderFastVideos(analytics.fastPerformers);
    this.renderAi(data.aiSuggestions, data.aiError);
    document.getElementById('limitations-list').innerHTML = data.limitations.map(item => `<li>${this.escape(this.text(`limit_${item}`))}</li>`).join('');
    results.scrollIntoView({ behavior: matchMedia('(prefers-reduced-motion: reduce)').matches ? 'auto' : 'smooth', block: 'start' });
  },
  renderNotices(data) {
    const notices = [`<div class="source-notice youtube">${this.escape(this.text('youtube_source'))}</div>`];
    if (data.source?.suggestions) notices.push(`<div class="source-notice ai">${this.escape(this.text('ai_source'))}</div>`);
    if (data.source?.stale) notices.push(`<div class="source-notice warning">${this.escape(this.text('stale'))}</div>`);
    document.getElementById('source-notices').innerHTML = notices.join('');
  },
  renderMetrics(analytics) {
    const w = analytics.windows['90'];
    const items = [
      [this.text('avg_views'), this.number(w.averageViews)],
      [this.text('avg_likes'), this.number(w.averageLikes)],
      [this.text('avg_comments'), this.number(w.averageComments)],
      [this.text('engagement'), this.percent(w.averageEngagementRate)],
      [this.text('uploads'), this.number(w.uploadsPerWeek, 1)],
      [this.text('fast_count'), this.number(analytics.fastPerformers.length)]
    ];
    document.getElementById('metrics-grid').innerHTML = items.map(([label,value]) => `<article class="niche-metric-card"><div class="niche-metric-value">${value}</div><div class="niche-metric-label">${this.escape(label)}</div></article>`).join('');
  },
  renderWindows(windows) {
    const rows = ['7','30','90'].map(days => {
      const w = windows[days];
      return `<tr><td>${days}d</td><td>${w.videoCount}</td><td>${this.number(w.medianViewsPerDay)}</td><td>${this.percent(w.medianEngagementRate)}</td><td>${this.number(w.uploadsPerWeek,1)}</td></tr>`;
    }).join('');
    document.getElementById('window-table').innerHTML = `<table class="window-table"><thead><tr><th>${this.text('window')}</th><th>${this.text('videos')}</th><th>${this.text('views_day')}</th><th>${this.text('engagement_short')}</th><th>${this.text('uploads_short')}</th></tr></thead><tbody>${rows}</tbody></table>`;
  },
  renderKeywords(keywords) {
    document.getElementById('keyword-list').innerHTML = keywords.length
      ? keywords.map(item => `<span class="keyword-chip">${this.escape(item.keyword)} <small>${item.count}</small></span>`).join('')
      : '—';
  },
  renderPatterns(patterns) {
    document.getElementById('pattern-list').innerHTML = patterns.length
      ? patterns.map(item => `<div class="pattern-item">${this.escape(this.text(`pattern_${item.pattern}`))} · ${item.count}</div>`).join('')
      : '—';
  },
  renderFastVideos(videos) {
    document.getElementById('fast-videos').innerHTML = videos.length ? videos.map(video =>
      `<a class="fast-video" href="${this.escape(video.url)}" target="_blank" rel="noopener"><img src="${this.escape(video.thumbnail)}" alt="" loading="lazy"><div><div class="fast-video-title">${this.escape(video.title)}</div><div class="fast-video-meta">${this.escape(video.channelTitle)} · ${this.number(video.viewsPerDay)} ${this.text('views_per_day')} · ${this.number(video.ageDays,0)} ${this.text('days_old')}</div></div></a>`
    ).join('') : `<div class="niche-empty">${this.escape(this.text('no_fast'))}</div>`;
  },
  renderAi(ai, error) {
    const container = document.getElementById('ai-content');
    if (!ai) { container.innerHTML = `<div class="niche-empty">${this.escape(error || this.text('ai_unavailable'))}</div>`; return; }
    const list = (title, items) => `<div class="ai-group"><h4>${this.escape(title)}</h4><div class="idea-list">${items.map(item => `<div class="idea-item">${this.escape(item)}</div>`).join('')}</div></div>`;
    container.innerHTML = `<h4>${this.escape(this.text('summary'))}</h4><p class="ai-summary">${this.escape(ai.summary)}</p>${list(this.text('titles'),ai.titleIdeas)}${list(this.text('tags'),ai.tagSuggestions)}${list(this.text('angles'),ai.videoAngles)}`;
  }
};

document.addEventListener('DOMContentLoaded', () => NicheInsights.init());
